const fs = require('fs');
const path = require('path');
const { poolPromise } = require('../db');

const OUTPUT_FILE = path.resolve(__dirname, '../../database/SQLdata.sql');

const TABLE_ORDER = [
	'Users',
	'StudentProfiles',
	'OrganizerProfiles',
	'Interests',
	'UserInterests',
	'EventCategories',
	'EventTags',
	'Skills',
	'Events',
	'EventCategoryMapping',
	'EventTagMapping',
	'EventSkillMapping',
	'EventRequests',
	'Registrations',
	'RegistrationWaitlist',
	'Teams',
	'TeamMembers',
	'Attendance',
	'StudentAchievements',
	'Certificates',
	'NotificationPreferences',
	'Notifications',
	'EventReviews',
	'ReviewResponses',
];

const pad2 = (n) => String(n).padStart(2, '0');
const pad3 = (n) => String(n).padStart(3, '0');

const formatDateTime = (d) => {
	const yyyy = d.getUTCFullYear();
	const mm = pad2(d.getUTCMonth() + 1);
	const dd = pad2(d.getUTCDate());
	const hh = pad2(d.getUTCHours());
	const mi = pad2(d.getUTCMinutes());
	const ss = pad2(d.getUTCSeconds());
	const ms = pad3(d.getUTCMilliseconds());
	return `${yyyy}-${mm}-${dd} ${hh}:${mi}:${ss}.${ms}`;
};

const escapeSqlString = (value) => String(value).replace(/'/g, "''");

const toSqlLiteral = (value, typeName) => {
	if (value === null || value === undefined) return 'NULL';

	const t = String(typeName || '').toLowerCase();
	if (t.includes('char') || t.includes('text') || t.includes('xml') || t.includes('uniqueidentifier')) {
		return `N'${escapeSqlString(value)}'`;
	}

	if (t === 'date') {
		const d = value instanceof Date ? value : new Date(value);
		if (Number.isNaN(d.getTime())) return `N'${escapeSqlString(value)}'`;
		return `N'${d.toISOString().slice(0, 10)}'`;
	}

	if (t.includes('datetime') || t.includes('smalldatetime') || t.includes('datetimeoffset') || t === 'time') {
		const d = value instanceof Date ? value : new Date(value);
		if (Number.isNaN(d.getTime())) return `N'${escapeSqlString(value)}'`;
		return `N'${formatDateTime(d)}'`;
	}

	if (typeof value === 'boolean') return value ? '1' : '0';
	if (typeof value === 'number') return Number.isFinite(value) ? String(value) : 'NULL';

	return `N'${escapeSqlString(value)}'`;
};

(async () => {
	const pool = await poolPromise;

	const metadataResult = await pool.request().query(`
		SELECT
			t.name AS TableName,
			c.name AS ColumnName,
			ty.name AS TypeName,
			c.column_id AS ColumnID,
			c.is_identity AS IsIdentity,
			c.is_computed AS IsComputed
		FROM sys.tables t
		JOIN sys.columns c ON t.object_id = c.object_id
		JOIN sys.types ty ON c.user_type_id = ty.user_type_id
		WHERE t.is_ms_shipped = 0
		ORDER BY t.name, c.column_id
	`);

	const tableToColumns = new Map();
	for (const row of metadataResult.recordset) {
		if (row.IsComputed) continue;
		const table = row.TableName;
		if (!tableToColumns.has(table)) tableToColumns.set(table, []);
		tableToColumns.get(table).push({
			name: row.ColumnName,
			typeName: row.TypeName,
			isIdentity: Boolean(row.IsIdentity),
			columnId: row.ColumnID,
		});
	}

	const lines = [];
	lines.push('-- =============================================================================');
	lines.push('-- SQL DATA SNAPSHOT (AUTO-GENERATED FROM LIVE DB)');
	lines.push('-- Generated by backend/scripts/export-db-to-sqldata.js');
	lines.push('-- Rerun-safe: clears existing data before reinserting snapshot rows');
	lines.push('-- =============================================================================');
	lines.push('USE [EventDhondo];');
	lines.push('GO');
	lines.push('');
	lines.push('-- Reset existing data (child tables first to satisfy FKs)');
	for (const tableName of [...TABLE_ORDER].reverse()) {
		if (!tableToColumns.has(tableName)) continue;
		lines.push(`DELETE FROM [dbo].[${tableName}];`);
	}
	lines.push('');

	for (const tableName of TABLE_ORDER) {
		const cols = tableToColumns.get(tableName) || [];
		if (cols.length === 0) continue;

		const orderedCols = [...cols].sort((a, b) => a.columnId - b.columnId);
		const columnList = orderedCols.map((c) => `[${c.name}]`).join(', ');
		const identityCols = orderedCols.filter((c) => c.isIdentity);

		const rowsResult = await pool.request().query(`SELECT * FROM [dbo].[${tableName}] ORDER BY 1`);
		const rows = rowsResult.recordset || [];

		lines.push(`-- ${tableName} (${rows.length} rows)`);

		if (rows.length === 0) {
			lines.push(`-- No rows in [${tableName}]`);
			lines.push('');
			continue;
		}

		if (identityCols.length > 0) {
			lines.push(`SET IDENTITY_INSERT [dbo].[${tableName}] ON;`);
		}

		lines.push(`INSERT INTO [dbo].[${tableName}] (${columnList}) VALUES`);

		rows.forEach((row, idx) => {
			const valueList = orderedCols
				.map((col) => toSqlLiteral(row[col.name], col.typeName))
				.join(', ');
			const suffix = idx === rows.length - 1 ? ';' : ',';
			lines.push(`(${valueList})${suffix}`);
		});

		if (identityCols.length > 0) {
			lines.push(`SET IDENTITY_INSERT [dbo].[${tableName}] OFF;`);
		}

		lines.push('');
	}

	fs.writeFileSync(OUTPUT_FILE, `${lines.join('\n')}\n`, 'utf8');
	console.log(`Snapshot written to ${OUTPUT_FILE}`);
	process.exit(0);
})().catch((err) => {
	console.error('Export failed:', err.message || err);
	process.exit(1);
});
